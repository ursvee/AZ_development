let count_members = 1;

while (count_members <= 5) { // check condition
    console.log(`Member counter: ${count_members}`); // do statement
    count_members++; // increment
}

console.log("End while");

let check_bal = 3;

do {
    console.log(`Sub counter: ${check_bal}`); // do statement
    check_bal++; // increment
} while (check_bal <= 5) // check condition

console.log("End do while");


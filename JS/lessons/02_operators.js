let num1 = 1
let num2 = 2

console.log(num1 + num2); // addition
console.log(num1 - num2); // subtraction
console.log(num1 * num2); // multiplication
console.log(num1 / num2); // division
console.log(num1 % num2); // modulus
console.log(num1++); // increment later
console.log(++num1); // first increment

console.log(num2--); // decrement later
console.log(--num2); // first decrement
console.log("end arithmetic");

num1 = 1
num2 = 2

console.log(num1 > num2);
console.log(num1 < num2);
console.log(num1 >= num2);
console.log(num1 <= num2);


let num3 = "10";
let num4 = 10;

console.log(num3 == num4); // value
console.log(num3 === num4); // value & type
console.log(num3 != num4); // not equal value
console.log(num3 !== num4); // not equal value & type
console.log("end comparisons");
